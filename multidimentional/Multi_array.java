package multidimentional;

public class Multi_array {

	public static void main(String[] args) {
	int [][]array={{7,2,3},{2,2,3},{4,8,5}};
	for(int i=0; i<array.length;i++) {
		
		for(int j=0;j<array[i].length;j++) {
			System.out.print(array[i][j]);
			
		}
		System.out.println("  ");
	}
		
	

	}

}
